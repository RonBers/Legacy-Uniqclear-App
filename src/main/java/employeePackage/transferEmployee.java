    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        branchLabel.setText(this.empBranch);
        nameLabel.setText(this.empName);
        roleLabel.setText(this.empRole);
        idLabel.setText(empID);
        
        
        String sql = "SELECT branch_name, branch_id FROM branch;";
        String currentBranch = branchLabel.getText().toString().trim();
        
        DefaultComboBoxModel model = (DefaultComboBoxModel)selectBranch.getModel();
        try{
          PreparedStatement pst = con.prepareStatement(sql);
          ResultSet rs = pst.executeQuery();
          while (rs.next()){
              String tempBranch = rs.getString("branch_name");
              String tempBranchID = rs.getString("branch_id");
              
              branches.put(tempBranchID,tempBranch);
              
              if (!tempBranch.matches(currentBranch)){
                  model.addElement(tempBranch);
              }
              // model.addElement(rs.getString("branch_name"));
              // 
             
              
              
          }
        }catch(Exception ex){
            System.out.println("Error: "+ex.getMessage());
        }
        selectBranch.setModel(model);
        
        
        
        
    }//GEN-LAST:event_formWindowOpened

