    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        cancelClicked = false;
        
        String branchName = selectBranch.getSelectedItem().toString();
        String branchID = "";
        for(Map.Entry m : branches.entrySet()){    
                if(m.getValue().toString().equalsIgnoreCase(branchName)){
                    branchID = m.getKey().toString();
                }    
        } 
      
        String updateEmployeeTable = "UPDATE employee SET branch_id = "+branchID+" WHERE employee_id = " +empID+";";
        try{
            PreparedStatement pst = con.prepareStatement(updateEmployeeTable);
            pst.executeUpdate();  
            
        }catch (Exception ex){
            System.out.println("Error: "+ ex.getMessage());
        }
        
        SimpleDateFormat dtf = new SimpleDateFormat("yyyy-MM-dd");
        String date ="'" +dtf.format(assignmentDate.getDate()) + "'";
        
        String addToEmployeeHistory = "INSERT INTO employee_branch_history (employee_id, branch_id, assignment_date) VALUES ("+empID+", "+branchID+","+date+");";
       
        try{
            PreparedStatement pst2 = con.prepareStatement(addToEmployeeHistory);
            pst2.executeUpdate(); 
        }catch(Exception ex){
            System.out.println("Error: "+ ex.getMessage());
        }
        this.dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

