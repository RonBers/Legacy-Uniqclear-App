    private void setCustomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setCustomButtonActionPerformed
        // TODO add your handling code here:
       // DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        SimpleDateFormat dtf = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat forText = new SimpleDateFormat("MMMM dd, yyyy");
        
        Date startFromCalendar = startCalendar.getDate();
        Date endFromCalendar = endCalendar.getDate();
        
        
        
        String start = dtf.format(startFromCalendar);
        String end = dtf.format(endFromCalendar);
        
       
        
        if (startFromCalendar.before(endFromCalendar)){
            this.startDate = start;
            this.endDate = end;
            
            customDateRange.setText(forText.format(startFromCalendar) + " - "+forText.format(endFromCalendar));
        }else{
            JOptionPane.showMessageDialog(this, "Please select a start date that occurs before the selected end date", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_setCustomButtonActionPerformed

    private void applyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyButtonActionPerformed
        // TODO add your handling code here:

        ActionEvent pressed = new ActionEvent(applyButton, 0, "Nothing");
            
        
        if (op1Clicked){
            dayButtonActionPerformed(pressed);
            this.dispose();
        }else if(op3Clicked){
            setWeekDateButtonActionPerformed(pressed);
            this.dispose();
        }else if (op4Clicked){
            setMonthButtonActionPerformed(pressed);
            this.dispose();
        }else if (op5Clicked){
            setCustomButtonActionPerformed(pressed);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"Please select custom date range first!","Error!", JOptionPane.WARNING_MESSAGE);
        }

        
    }//GEN-LAST:event_applyButtonActionPerformed

