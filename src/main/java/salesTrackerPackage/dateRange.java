    private void setTodayDate(){
        DateFormat dtf = new SimpleDateFormat("yyyy-MM-dd");
        DateTimeFormatter dtf2 = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String dateInString = dtf2.format(today);
         
        try{
            Date setDate = (Date)dtf.parse(dateInString);
            dayChooser.setDate(setDate);
        }catch(Exception ex){
            System.out.println("Error: " +ex.getMessage());
        }
        
        
      //  Date setToday = dtf.format(today);
  
    }
    
    
    
    
    private void setWeekDate(LocalDate date){
       
        LocalDate startDay = date.with(DayOfWeek.MONDAY);
        LocalDate endDay = date.with(DayOfWeek.SATURDAY);
        DateTimeFormatter dtf2 = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        DateTimeFormatter dtf3 = DateTimeFormatter.ofPattern("MMMM dd, yyyy");
        String startDateInString = dtf2.format(startDay);
        String endDateInString = dtf2.format(endDay);
        String dateRangeText = dtf3.format(startDay) +" - "+ dtf3.format(endDay);
            
        dateRange.setText(dateRangeText);
        
        
        this.startDate = startDateInString;
        this.endDate = endDateInString;
        
    }
    
    private void setMonthDate(LocalDate date){

        YearMonth yearMonth = YearMonth.from(date);
        
        
        LocalDate startDay = yearMonth.atDay(1);
        LocalDate endDay = yearMonth.atEndOfMonth();
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String startDateInString = dtf.format(startDay);
        String endDateInString = dtf.format(endDay);
        
        
        DateTimeFormatter forText = DateTimeFormatter.ofPattern("MMMM dd, yyyy");
        
        monthDateRange.setText(forText.format(startDay)+" - "+forText.format(endDay));
        
        this.startDate = startDateInString;
        this.endDate = endDateInString;
    }
    
       
    
    
