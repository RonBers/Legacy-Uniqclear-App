    private void setDefaultDate(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String today = dtf.format(LocalDateTime.now());
        
        String dateTimeSplit[] = today.split(" ", 2);
        
        String datePart = dateTimeSplit[0];
        String time = dateTimeSplit[1];
        String timeParts[] = time.split(":",3);
        
        setHour.setValue(Integer.parseInt(timeParts[0]));
        setMinute.setValue(Integer.parseInt(timeParts[1]));
        setSeconds.setValue(Integer.parseInt(timeParts[2]));
           
        DateFormat formatter = new SimpleDateFormat("yyyy-MM-dd"); 
        
        try{
            Date defaultDate = (Date)formatter.parse(datePart);
            rentDateChooser.setDate(defaultDate);
            
        }catch(Exception ex){
            System.out.println("Error: "+ex.getMessage());
        }
             
    }
