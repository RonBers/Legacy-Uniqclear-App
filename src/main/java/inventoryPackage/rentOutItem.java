    private void setCustomerValues(String id){
        String sql = "SELECT concat(last_name,',',first_name, ' ',middle_name) as name, customer_address, contact_num FROM customer WHERE customer_id = " +id+";";
        this.custID=id;
        try{
            PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        while(rs.next()){
            customerName.setText(rs.getString("name"));
            customerAddress.setText(rs.getString("customer_address"));
            customerContact.setText(rs.getString("contact_num"));
        }
        }catch(Exception ex){
            System.out.println("Error: " + ex.getMessage());
        }
       
    }
    
    
