    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String sql = "SELECT * FROM rented_out WHERE rental_item_id = " +rental_item_id+" ORDER BY out_datetime DESC LIMIT 1;";
        String custId ="";
        String itemId="";
        String dateTime = "";
        
        try{
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                custId = rs.getString("customer_id");
                itemId = rs.getString("rental_item_id");
                dateTime= rs.getString("out_datetime");
                this.rented_out_id=rs.getString("rented_out_id");
            }
        }catch(Exception ex){
            System.out.println("Error: "+ex.getMessage());
        }
        
        customerID.setText(custId);
        itemID.setText(itemId);
        customerName.setText(getCustName(custId));
        rentalItemName.setText(getItemName(itemId));
        dateRented.setText(dateTime);
        setDefaultDate();
    }//GEN-LAST:event_formWindowOpened

