    public void generateTable(){
        table = (DefaultTableModel) Promos.getModel();
        table.setRowCount(0); // To print from the top
        
        String sql="SELECT promo_id, promo_description, promo_discount_amount from promo";
        
        try{
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String id = rs.getString("promo_id"); // Set id to id from database
                String desc = rs.getString("promo_description");
                String discount = rs.getString("promo_discount_amount");
                table.addRow(new Object[]{id, desc, discount}); // Array to add as a row in the table
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        } 
    }
