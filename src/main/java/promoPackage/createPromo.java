    private void addPromoBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPromoBTNActionPerformed
        // JOptionPane.showMessageDialog(null, "Promo added!");
        String description = promoDesc.getText();
        int discountValue = (int) discountVal.getValue();
        String regexCheckNum = "\\d+";
        
        try{
            if(description.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please add a description!", "Error", JOptionPane.INFORMATION_MESSAGE);
            }else if(discountValue==0 || !String.valueOf(discountValue).matches(regexCheckNum)){
                JOptionPane.showMessageDialog(this, "Please add a discount!", "Error", JOptionPane.INFORMATION_MESSAGE);
            }else{
                String sql = "INSERT into promo(promo_description, promo_discount_amount, branch_id) VALUES('"+description+"','"+discountValue+"','"+3+"')";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Promo added!", "Add promo", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
        generateTable();
    }//GEN-LAST:event_addPromoBTNActionPerformed

