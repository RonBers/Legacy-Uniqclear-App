    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String sql = "SELECT promo_id, CONCAT(last_name, ', ', first_name), promo_effect_count "
                + "FROM promo_record join customer on promo_record.customer_id=customer.customer_id"; // SQL command to view all information from the table
        
        try{
            pst = con.prepareStatement(sql); // 
            ResultSet rs = pst.executeQuery(); // Java object that represents the result of a database query, which contains the data retrieved from the database.
            DefaultTableModel customPromoTable = (DefaultTableModel) promoTable.getModel(); // studentTable = Table in GUI
            
            customPromoTable.setRowCount(0); // To print from the top
            
            while(rs.next()){
                int promoID = rs.getInt("promo_id");
                String name = rs.getString("name");
                int points = rs.getInt("promo_effect_count");
                
                customPromoTable.addRow(new Object[] {promoID,name,points});
            }
        }catch(Exception e){
            System.out.println("Failed"+ e.getMessage());
        }
        
    }//GEN-LAST:event_formWindowOpened

